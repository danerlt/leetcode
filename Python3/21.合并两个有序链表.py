# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def mergeTwoLists(self, list1: Optional[ListNode], list2: Optional[ListNode]) -> Optional[ListNode]:
        dump = ListNode()
        head = dump
        p1 = list1
        p2 = list2
        while p1 and p2:
            if p1.val > p2.val:
                dump.next = p2
                dump = dump.next
                p2 = p2.next
            else:
                dump.next = p1
                dump = dump.next
                p1 = p1.next

        if p1:
            dump.next = p1
        if p2:
            dump.next = p2

        return head.next



