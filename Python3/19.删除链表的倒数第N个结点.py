class Solution:
    def removeNthFromEnd(self, head: Optional[ListNode], n: int) -> Optional[ListNode]:
        # æ³¨æ„è¾¹ç•Œæƒ…å†µ
        dummy = ListNode(-1)
        dummy.next = head

        p1 = head
        for i in range(n):
            p1 = p1.next

        p2 = dummy
        while p1:
            p1 = p1.next
            p2 = p2.next

        p2.next = p2.next.next

        return dummy.next

