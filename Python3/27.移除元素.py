class Solution:
    def removeElement(self, nums: List[int], val: int) -> int:
        # è§£é¢˜æ€è·¯ åŒæŒ‡é’ˆ
        # fasterï¼Œlowerä»length-1å¾€å‰èµ°
        # å¦‚æœ lowerçš„å€¼ç­‰äºval åˆ™ lower--, faster--
        # å¦‚æœ lowerçš„å€¼ä¸ç­‰äºvalï¼Œfasterå¾€å‰æ‰¾valï¼Œæ‰¾åˆ°ä¹‹åäº¤æ¢lowerï¼Œfasterçš„å€¼ï¼Œç„¶ålower--,faster--
        if not nums:
            return 0
        faster, lower = len(nums) - 1, len(nums) - 1
        while faster >= 0:
            if nums[lower] == val:
                lower -= 1
                faster -= 1
            elif nums[faster] == val:
                nums[lower], nums[faster] = nums[faster], nums[lower]
                lower -= 1
                faster -= 1
            else:
                faster -= 1

        return lower + 1


